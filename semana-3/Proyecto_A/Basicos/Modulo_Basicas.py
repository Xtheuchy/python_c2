#Metodo de procesos basicos
def Metodo_suma(A,B):
    return A+B
def Metodo_resta(A,B):
    return A-B
def Metodo_multiplica(A,B):
    return A*B
def Metodo_divide(A,B):
    return A/B
def Metodo_Imprimir_Basicos(p,q):
    print("Suma                : ", Metodo_suma(p,q))
    print("Resta               : ", Metodo_resta(p,q))
    print("Multiplicaci√≥n     : ", Metodo_multiplica(p,q))
    print("Division            : ", Metodo_divide(p,q))